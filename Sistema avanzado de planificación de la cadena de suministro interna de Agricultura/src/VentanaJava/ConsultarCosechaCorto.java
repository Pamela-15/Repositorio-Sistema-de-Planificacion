    private void ListaPlanesAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_ListaPlanesAncestorAdded
        
        DefaultListModel modelo = new DefaultListModel();
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        String driver = "com.mysql.jdbc.Driver";
        String connection = "jdbc:mysql://localhost:3306/Cargill";
        String user = "root";
        String password = "admi";
        try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(connection, user, password);
            if (!con.isClosed()) {
                PreparedStatement cosechacorto = con.prepareStatement("SELECT distinct `cosecha corto plazo`.`Fecha de cosecha` FROM cargill.`cosecha corto plazo` where `Fecha de cosecha`>? ");
                cosechacorto.setString(1, dateFormat.format(date));
                ResultSet planescosechacorto = cosechacorto.executeQuery();
                while( planescosechacorto.next() ) {
                    modelo.addElement(planescosechacorto.getString("Fecha de cosecha"));
                }
                planescosechacorto.close();
                cosechacorto.close();
                con.close();
                ListaPlanes.setModel(modelo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage(),"Error", JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_ListaPlanesAncestorAdded

