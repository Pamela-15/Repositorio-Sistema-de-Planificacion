    private void ListaIngresoCortoAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_ListaIngresoCortoAncestorAdded
        DefaultListModel modelo = new DefaultListModel();
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        String driver = "com.mysql.jdbc.Driver";
        String connection = "jdbc:mysql://localhost:3306/Cargill";
        String user = "root";
        String password = "admi";
        try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(connection, user, password);
            if (!con.isClosed()) {
                PreparedStatement ingresocorto = con.prepareStatement("SELECT distinct `ingreso corto plazo`.`Fecha de Ingreso` FROM cargill.`ingreso corto plazo` where `Fecha de Ingreso`>?;");
                ingresocorto.setString(1, dateFormat.format(date));
                ResultSet planesingresocorto = ingresocorto.executeQuery();
                while( planesingresocorto.next() ) {
                    modelo.addElement(planesingresocorto.getString("Fecha de Ingreso"));
                }
                planesingresocorto.close();
                ingresocorto.close();
                con.close();
                ListaIngresoCorto.setModel(modelo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage(),"Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ListaIngresoCortoAncestorAdded

