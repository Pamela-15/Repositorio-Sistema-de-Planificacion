    private void ListaIngresoMedianoAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_ListaIngresoMedianoAncestorAdded
        DefaultListModel modelo = new DefaultListModel();
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        String driver = "com.mysql.jdbc.Driver";
        String connection = "jdbc:mysql://localhost:3306/Cargill";
        String user = "root";
        String password = "admi";
        try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(connection, user, password);
            if (!con.isClosed()) {
                PreparedStatement ingresolargo = con.prepareStatement("SELECT distinct month(`ingreso mediano plazo`.`Fecha de ingreso`) FROM `cargill`.`ingreso mediano plazo`; where `Fecha de ingreso`>? ");
                ingresolargo.setString(1, dateFormat.format(date));
                ResultSet planesingresolargo = ingresolargo.executeQuery();
                while( planesingresolargo.next() ) {
                    modelo.addElement(planesingresolargo.getString("month(`ingreso mediano plazo`.`Fecha de ingreso`)"));
                }
                planesingresolargo.close();
                ingresolargo.close();
                con.close();
                ListaIngresoMediano.setModel(modelo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage(),"Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ListaIngresoMedianoAncestorAdded

