    private void EliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarUsuarioActionPerformed
        int galeraeliminar=0;
        DefaultTableModel model=(DefaultTableModel) TablaAdministracionUsuarios.getModel();
        if( TablaAdministracionUsuarios.getSelectedRow() < 0 ){
            JOptionPane.showMessageDialog(null, "No ha seleccionado ninguna fila para eliminar","Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
            galeraeliminar=Integer.parseInt(model.getValueAt(TablaAdministracionUsuarios.getSelectedRow(),1).toString());
            String driver = "com.mysql.jdbc.Driver";
            String connection = "jdbc:mysql://localhost:3306/Cargill";
            String user = "root";
            String password = "admi";
            try {
                Class.forName(driver);
                Connection con = DriverManager.getConnection(connection, user, password);
                if (!con.isClosed()) {
                    PreparedStatement eliminarusuarios = con.prepareStatement("DELETE FROM `usuario` WHERE `idUsuario` = ?;");
                    eliminarusuarios.setInt(1, galeraeliminar);
                    int x = eliminarusuarios.executeUpdate();
                    eliminarusuarios.close();
                    con.close();
                    
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error",e.getMessage(), JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_EliminarUsuarioActionPerformed

