    private void NumeroGaleraAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_NumeroGaleraAncestorAdded
        String driver = "com.mysql.jdbc.Driver";
        String connection = "jdbc:mysql://localhost:3306/cargill";
        String user = "root";
        String password = "admi";
        try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(connection, user, password);
                           
            if (!con.isClosed()) {
                PreparedStatement galera = con.prepareStatement("select * from `galera` Where `idGalera` = ? ");
                galera.setInt(1,idGaleraModificar);
                ResultSet resultadogalera = galera.executeQuery();
                while( resultadogalera.next() ) {
                    NumeroGalera.setText("" + resultadogalera.getInt("Numero de Galera"));
                    NombreGranja.setText(resultadogalera.getString("Nombre Granja"));
                    Zona.setText(resultadogalera.getString("Zona"));
                    Capacidad.setText("" + resultadogalera.getInt("Capacidad"));
                    Carrusel.setSelectedItem(resultadogalera.getString("Carrusel")); 
                    Supervisor.setSelectedItem(resultadogalera.getString("Supervisor")); 
                }
                resultadogalera.close();
                galera.close();
                con.close();
            }              
                
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Los datos ingresados no están en el formato aceptado" ,"Error", JOptionPane.ERROR_MESSAGE);
        }// TODO add your handling code here:
    }//GEN-LAST:event_NumeroGaleraAncestorAdded

