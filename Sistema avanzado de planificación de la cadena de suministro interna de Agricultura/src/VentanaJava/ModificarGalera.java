    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        String driver = "com.mysql.jdbc.Driver";
        String connection = "jdbc:mysql://localhost:3306/cargill";
        String user = "root";
        String password = "admi";
        try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(connection, user, password);

            if (!con.isClosed()&& NumeroGalera.getText() != null && NombreGranja.getText()!= null && Zona.getText()!= null && Capacidad.getText()!= null && Carrusel.getSelectedItem()!= null && Supervisor.getSelectedItem()!= null) {

                PreparedStatement galera = con.prepareStatement("UPDATE `galera` SET `Numero de Galera` = ?, `Nombre Granja` = ?, `Zona` = ?, `Capacidad` = ?, `Carrusel` = ?, `Supervisor` = ? WHERE `idGalera` = ?;");
                galera.setInt(7, idGaleraModificar);
                galera.setInt(1, Integer.parseInt(NumeroGalera.getText()));
                galera.setString(2, NombreGranja.getText());
                galera.setString(3, Zona.getText());
                galera.setInt(4, Integer.parseInt(Capacidad.getText()));
                galera.setString(5, Carrusel.getSelectedItem().toString());
                galera.setString(6, Supervisor.getSelectedItem().toString());
                int x = galera.executeUpdate();
                galera.close();
                con.close();
                VentanaJava.Galeras.ActualizarTabla();
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(null, "Campos obligatorios incompletos","Por favor ingrese los datos en todos los campos mostrados", JOptionPane.ERROR_MESSAGE);
                con.close();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage(),"Los datos ingresados no están en el formato aceptado", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_GuardarActionPerformed

